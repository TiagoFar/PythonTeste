num = [2, 5, 9, 8] #aula para ir aprendendo os comandos novos
num[1] = 7
num.append(6) #ADICIONA O NÚMERO 6 NA ULTIMA
num.sort() #ORGANIZA
num.sort(reverse=True) #ORGANIZA DE TRAS PRA FRENTE
num.insert(0, 2) # coloco na posição ZERO o numero DOIS
num.pop() #EXCLUI O ÚLTIMO elemento
num.pop(0) #EXCLUI O PRIMEIRO ELEMENTO, 'ZERO'.
num.insert(4, 2) #Na POSICAO 4, COLOCO O NÚMERO 2!
num.insert(4, 2) # inseri mais um DOIS na ultima posicao
num.remove(2) #exclui o primeiro termo com número 2!
print(f'Essa lista tem {len(num)} elementos') # MOSTRA QUANTOS ELEMENTOS TEM NA LISTA
print(num)




